#include <stdio.h>
#include <stdlib.h>
#include "ft_printf.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*str;

	str = malloc(count * size);
	if (!str)
		return (NULL);
	ft_bzero(str, (count * size));
	return ((void *)str);
}
